/**Data type example programs*/
public class variable {
public static void main(String args[])
{
	char  ch='m';
	String str="Java";
	int n=120;
	float d=7.5f;
	boolean f=false;
	System.out.println("Char is:"+ch);
	System.out.println("String is:"+str);
	System.out.println("Integer is:"+n);
	System.out.println("Floating is:"+d);
	System.out.println("Boolean is:"+f);
}
}
